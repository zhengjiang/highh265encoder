static inline int bs_size_ue( unsigned int val )
{
    return bitSize[val+1];
}

static inline int bs_size_ue_big( unsigned int val )
{
    if( val < 255 )
        return bitSize[val+1];
    else
        return bitSize[(val+1)>>8] + 16;
}

static inline int bs_size_se( int val )
{
    int tmp = 1 - val*2;
    if( tmp < 0 ) tmp = val*2;
    if( tmp < 256 )
        return bitSize[tmp];
    else
        return bitSize[tmp>>8]+16;
}

